 ## Comments and Print Statements
 */

/*:
 ![speech bubble](speech-bubble.png)
 */

/*:
Print statements allow us to write code to the console that does not affect or appear in our app. Print statements are intended for debugging errors or logging messages to our console. Let's create our very first print statement.
 
    print("Hello World!")
 
 If you run the code above, you will see "Hello World!" printed in the console.
 
 Comments are intended to write useful descriptions of what code does. Any code wrapped in a comment will not run.
 
 */
