/*:
 ## Types and Variables - Naming
 
 It is best practice to use *camel case* naming convention. This means using all lower case for the first word in a variable name and using upper case for the first letter of every subsequent word.
 */

/*:
 ![camel case](camel.png)
 */

/*:
 
 For example, assume you would like to name a variable account balance. You would do so as follows in camel case:
 
    var accountBalance = 200.45
 
 In the example above, you declare a variable named `accountBalance` using camel case and assign it a value of 200.45. It is inferred to be of type `Double`.
 
 You can name a variable or constant whatever you want but it is best to use descriptive and self-explanatory names. The [Swift API Design Guidelines](https://swift.org/documentation/api-design-guidelines/) gives us further guidance on this.
 
 * Include all the words needed to avoid ambiguity.
 * Omit needless words.
 * Name variables, parameters, and associated types according to their roles.
 
 There are limitations on how you name variables or constants. For example, you can not use whitespace, use mathematical symbols or start a variable/constant name with a number.
*/
